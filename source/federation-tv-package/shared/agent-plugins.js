// Plugin Manager
class PluginManager {
    constructor(registry) {
        this.registry = registry;
        this.plugins = new Map();
        this.loadOrder = [];
    }

    /**
     * Load built-in plugins
     */
    loadBuiltins() {
        const builtins = [
            new AvatarPlugin(),
            new RoomPlugin(),
            new FederationPlugin(),
            new HealthPlugin(),
            new TVRoomPlugin()
        ];

        builtins.forEach(plugin => this.loadPlugin(plugin));
    }

    /**
     * Load a plugin
     */
    loadPlugin(plugin) {
        if (this.plugins.has(plugin.name)) {
            console.warn(`[PluginManager] Plugin ${plugin.name} already loaded`);
            return false;
        }

        try {
            plugin.install(this.registry);
            this.plugins.set(plugin.name, plugin);
            this.loadOrder.push(plugin.name);
            return true;
        } catch (error) {
            console.error(`[PluginManager] Failed to load ${plugin.name}:`, error);
            return false;
        }
    }

    /**
     * Load plugin from file/module
     */
    async loadPluginFromModule(modulePath) {
        try {
            const module = await import(modulePath);
            const PluginClass = module.default || module.Plugin || module[Object.keys(module)[0]];
            if (PluginClass && PluginClass.prototype instanceof AgentPlugin) {
                return this.loadPlugin(new PluginClass());
            }
            throw new Error('Invalid plugin module');
        } catch (error) {
            console.error(`[PluginManager] Failed to load from ${modulePath}:`, error);
            return false;
        }
    }

    /**
     * Unload plugin
     */
    unloadPlugin(name) {
        const plugin = this.plugins.get(name);
        if (plugin) {
            plugin.uninstall();
            this.plugins.delete(name);
            this.loadOrder = this.loadOrder.filter(n => n !== name);
            return true;
        }
        return false;
    }

    /**
     * Get plugin
     */
    getPlugin(name) {
        return this.plugins.get(name);
    }

    /**
     * Get all plugins
     */
    getAllPlugins() {
        return Array.from(this.plugins.values());
    }

    /**
     * Get plugins with capability
     */
    getPluginsWithCapability(capability) {
        return this.getAllPlugins().filter(p => p.hasCapability(capability));
    }

    /**
     * Execute hooks across all plugins
     */
    async executeHooks(event, data) {
        for (const plugin of this.plugins.values()) {
            await plugin.executeHooks(event, data);
        }
    }
}

