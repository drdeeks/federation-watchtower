// Federation Sync Plugin
class FederationPlugin extends AgentPlugin {
    constructor() {
        super('federation-sync', '1.0.0');
    }

    registerCapabilities() {
        this.addCapability('federation:register');
        this.addCapability('federation:sync');
        this.addCapability('federation:heartbeat');
    }

    registerHooks() {
        this.hook('agent:register', async (data) => {
            if (data.agent && this.registry) {
                await this.registry.registerWithFederation(data.agent);
            }
        }, 5);

        this.hook('agent:remove', async (data) => {
            if (this.registry) {
                try {
                    await fetch(`${this.registry.config.federationUrl}/api/leave`, {
                        method: 'POST',
                        headers: { 'Content-Type': 'application/json' },
                        body: JSON.stringify({ agentId: data.agentId, projectId: this.registry.config.projectId })
                    });
                } catch (e) { /* ignore */ }
            }
        });
    }
}

