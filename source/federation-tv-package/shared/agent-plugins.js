/**
 * Agent Plugin System — Production Grade
 * 
 * Allows agents to register capabilities, hooks, and extensions
 * that integrate with the registry and federation.
 */

class AgentPlugin {
    constructor(name, version = '1.0.0') {
        this.name = name;
        this.version = version;
        this.hooks = new Map();
        this.capabilities = new Set();
        this.registry = null;
    }

    /**
     * Install plugin into registry
     */
    install(registry) {
        this.registry = registry;
        this.registerHooks();
        this.registerCapabilities();
        console.log(`[Plugin] ${this.name} v${this.version} installed`);
    }

    /**
     * Uninstall plugin
     */
    uninstall() {
        this.hooks.clear();
        this.capabilities.clear();
        this.registry = null;
        console.log(`[Plugin] ${this.name} uninstalled`);
    }

    /**
     * Override to register hooks
     */
    registerHooks() {}

    /**
     * Override to register capabilities
     */
    registerCapabilities() {}

    /**
     * Register a hook
     */
    hook(event, handler, priority = 0) {
        if (!this.hooks.has(event)) {
            this.hooks.set(event, []);
        }
        this.hooks.get(event).push({ handler, priority, plugin: this.name });
        this.hooks.get(event).sort((a, b) => b.priority - a.priority);
    }

    /**
     * Execute hooks for event
     */
    async executeHooks(event, data) {
        const hooks = this.hooks.get(event) || [];
        for (const { handler, plugin } of hooks) {
            try {
                await handler(data, this.registry);
            } catch (error) {
                console.error(`[Plugin:${plugin}] Hook ${event} failed:`, error);
            }
        }
    }

    /**
     * Add capability
     */
    addCapability(capability) {
        this.capabilities.add(capability);
        if (this.registry) {
            this.registry.emit('capabilityRegistered', { plugin: this.name, capability });
        }
    }

    /**
     * Check if plugin has capability
     */
    hasCapability(capability) {
        return this.capabilities.has(capability);
    }
}

